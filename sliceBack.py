letters = "abcdefghijklmnopqrstuvwxyz"

# prints out the string backwards
backwards = letters[25::-1]
print(backwards)

# prints qpo
qpo = letters[16:13:-1]
print(qpo)

# prints ebcba
ebcba = letters[4::-1]
print(ebcba)

# prints the last 8 characters in order
last8 = letters[-1:-9:-1]
print(last8)
